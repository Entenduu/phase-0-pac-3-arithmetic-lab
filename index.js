function add(x,y){
    return x + y;
}

function subtract(x,y){
    return x - y;
}

function multiply(x,y){
    return x * y;
}

function divide(x,y){
    return x/y;
}

function increment(n){
    let number = n;
    number++;
    return number;
}

function decrement(n){
    let number = n;
    number--;
    return number;
}

function makeInt(n){
    return parseInt(n, 10);
}

function preserveDecimal(n){
    return parseFloat(n);
}